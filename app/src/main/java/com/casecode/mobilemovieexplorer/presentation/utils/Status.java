package com.casecode.mobilemovieexplorer.presentation.utils;

/**
 * Enum representing the status of a data resource.
 * Possible values include SUCCESS, ERROR, LOADING, and NULL.
 */
public enum Status {

    /**
     * Indicates a successful state of a data resource.
     */
    SUCCESS,

    /**
     * Indicates an error state of a data resource.
     */
    ERROR,

    /**
     * Indicates a loading state of a data resource.
     */
    LOADING,

    /**
     * Indicates a null or empty state of a data resource.
     */
    NULL
}